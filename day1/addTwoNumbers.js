function addTwoNUmbers(numA, numB) {
  return numA + numB;
}
const numA = 5;
const numB = 10;
console.log(addTwoNUmbers(numA, numB)); // Output: 15

//another example
const result = addTwoNUmbers(numA, numB);
console.log(result); // Output: 15

//another example
console.log(addTwoNUmbers(20, 30)); // Output: 50
